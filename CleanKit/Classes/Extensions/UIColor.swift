import UIKit

extension UIColor {
    
    // MARK: - Initializer
    
    convenience init(hex: Int) {
        self.init(red: CGFloat((hex >> 16) & 0xff) / 255.0, green: CGFloat((hex >> 8) & 0xff) / 255.0, blue: CGFloat(hex & 0xff) / 255.0, alpha: CGFloat(1))
    }
}
